// =========================================================================
// 🚀 Step 1: シングルスレッドのTCPサーバー（土台作り）
// =========================================================================

// 🎯 【目的】
// ブラウザ（クライアント）からのアクセスを受け付け、HTTPリクエストを読み取り、
// 適切なレスポンス（HTML）を返す基本的なWebサーバーとしての土台を作る。

// 💡 【解決策と使用した機能】
// - `std::net::TcpListener`: 指定したポート（7878など）で接続を待ち受ける（bind）。
// - `listener.incoming()`: 接続要求のストリーム（TcpStream）を次々と取得するイテレータ。
// - `std::io::prelude::*`: Read・Writeトレイト。ストリームからデータを読み書きする。
//
// ⚠️ 【この時点での課題】
// シングルスレッドで動いているため、1つのリクエストの処理が終わるまで
// 次のリクエストを一切処理できない（直列処理）。
/* 
fn start_single_threaded_server() {
    let listener = TcpListener::bind("127.0.0.1:7878").unwrap();
    // 順番に1つずつリクエストを処理する
    for stream in listener.incoming() {
        let stream = stream.unwrap();
        handle_connection(stream); // この処理が遅いと、全ての後続リクエストが詰まる！
    }
}
*/


// =========================================================================
// 🚀 Step 2: 意図的な遅延（スレッドブロック）のシミュレーション
// =========================================================================

// 🎯 【目的】
// 「シングルスレッドのままでは、重い処理が来たときにサーバー全体が停止する」
// という問題を可視化し、並行処理を導入するための動機付けを行う。

// 💡 【解決策と使用した機能】
// - `std::thread::sleep`: 指定した時間、現在のスレッドの実行を一時停止（ブロック）する。
// - `/sleep` というパスへのリクエスト時に5秒間のスリープを挟むように変更。
//
// ⚠️ 【この時点での課題】
// `/sleep` にアクセスしたブラウザを開いたまま、別タブで `/` にアクセスすると、
// 最初の5秒間が終わるまで全くページが表示されない（ブロックされている状態）。


// =========================================================================
// 🚀 Step 3: 際限のないスレッド生成（thread::spawn）
// =========================================================================

// 🎯 【目的】
// ブロック問題を解決するため、リクエストが来るたびに新しいスレッドを生成し、
// それを並行処理させることで待機時間をなくす。

// 💡 【解決策と使用した機能】
// - `std::thread::spawn`: OSレベルの新しいスレッドを作成し、その中でクロージャを実行。
// - `move ||`: メインスレッドから `stream` の所有権を新しいスレッドに移動させる。
//
// ⚠️ 【この時点での課題】
// アクセスが来るたびにスレッドを生成すると、もしDoS攻撃などで1万件のアクセスが来たら、
// 1万個のスレッドが立ち上がり、サーバーのメモリとCPUが枯渇してクラッシュ（ダウン）する。
/*
    for stream in listener.incoming() {
        let stream = stream.unwrap();
        // 処理を別スレッドに丸投げ！（しかしリソース制限がない）
        thread::spawn(|| {
            handle_connection(stream);
        });
    }
*/


// =========================================================================
// 🚀 Step 4: スレッドプールの設計と導入（資源の制限と再利用）
// =========================================================================

// 🎯 【目的】
// サーバーダウンを防ぐため、あらかじめ決まった数（例：4つ）のスレッドだけを作成しておき、
// 新しいタスクが来たら「空いている手（スレッド）」に順番に処理を任せる仕組みを作る。

// 💡 【解決策と使用した機能】
// - `ThreadPool` 構造体: スレッドの群れを管理する窓口。
// - `pool.execute(|| { ... })`: 実行したい処理（クロージャ）を受け取るためのインターフェース。
//
// ⚠️ 【設計の壁】
// 「クロージャ」を引数として受け取り、それを後で別スレッドから呼び出せるようにするには、
// 複雑な型制約をクリアしなければならない。


// =========================================================================
// 🚀 Step 5: executeメソッドの型シグネチャ（クロージャを渡す）
// =========================================================================

// 🎯 【目的】
// `execute` メソッドが任意の「処理（クロージャ）」を受け取れるようにする。

// 💡 【解決策と使用した機能】
// `execute<F>(&self, f: F) where F: FnOnce() + Send + 'static` という制約の集合。
// - `FnOnce()`: 一度だけ実行されるクロージャ（クロージャの種類）。
// - `Send`: 別スレッドに安全に所有権を送れること（マルチスレッドのお約束）。
// - `'static`: メインスレッドが先に終了する可能性があるため、クロージャ内の借用が
//               「プログラム全体の実行期間と同じ」か「すべて所有権を持っている」こと。
// - `Box<dyn FnOnce...>`: クロージャのサイズはコンパイル時に不定なので、ヒープに確保してBox化（動的ディスパッチ）。


// =========================================================================
// 🚀 Step 6: チャンネル（mpsc）による仕事の受け渡し（生産者と消費者）
// =========================================================================

// 🎯 【目的】
// メインスレッド（ThreadPool）で受け取った「処理（Job）」を、
// 待機しているバックグラウンドの「ワーカー（Worker）」に安全に届ける。

// 💡 【解決策と使用した機能】
// - `std::sync::mpsc::channel`: 複数生成者・単一消費者（Multiple Producer, Single Consumer）の通信路。
//   - ThreadPool 側が `Sender` を持ちタスクを送信する。
//   - Worker 側が `Receiver` を持ちタスクを受信する。
//
// ⚠️ 【型システムの壁】
// `Worker` は複数いるのに、mpsc（Single Consumer）の `Receiver` は「単独」でしか持てない。
// 複数のスレッドで1つの `Receiver` を共有して「早い者勝ち」でタスクを取らせるにはどうするか？


// =========================================================================
// 🚀 Step 7: Arc と Mutex による状態の共有（安全な競合）
// =========================================================================

// 🎯 【目的】
// 複数のワーカー（別スレッド）で、1つの `Receiver` を共有しながら、
// 同時に同じメッセージを受け取ってしまわないように排他制御（ロック）をかける。

// 💡 【解決策と使用した機能】
// `Arc<Mutex<mpsc::Receiver<Job>>>` というRustの黄金パターン！
// - `Arc`: アトミック参照カウント。複数のスレッドで「安全に所有権を共有」する。
// - `Mutex`: ミューテックス。タスクを受信しようとする瞬間だけ「ロック」を獲得し、
//            他のスレッドが同時にアクセスできないようにブロックする（排他制御）。
// 
// これにより、Receiverの `recv()` は「ロックを獲得できた1つのワーカーだけ」が
// 次のタスクを取り出す（早い者勝ちパターン）という優雅な設計が完成した。


// =========================================================================
// 🚀 Step 8: Graceful Shutdown（優雅な終了）と Drop トレイト
// =========================================================================

// 🎯 【目的】
// Ctrl+Cなどでサーバーを終了させる際、現在処理中のブラウザのリクエストを
// 途中で切断しないように、「処理中のタスクが終わるのを待ってから」プロセスを終了させる。

// 💡 【解決策と使用した機能】
// 1. `enum Message { NewJob(Job), Terminate }`:
//    送信するデータを「タスクの追加」と「終了命令」の2つに分岐させる。
// 2. `Drop` トレイト:
//    ThreadPoolがスコープを抜けて破棄される際に自動発動するメソッド。
// 3. 全ワーカーへの終了命令 (`Terminate`) 送信。
// 4. `Option::take()` による所有権の奪取と `join`:
//    構造体(Worker)の一部である `JoinHandle` に対し、`join(self)` を呼んで待機するため、
//    フィールドを `Option` で包み、`take()` で「ムーブの穴」を作らずに中身を抜き出して `join` する。


// =========================================================================
// 🌟 20章を通じて得られた「プログラムへの哲学」
// =========================================================================
// 「ただ動くものを作る」だけなら、`thread::spawn` を乱発するか、高級なフレームワークを使えば一瞬で終わる。
// しかし、本質的な複雑さ（リソース枯渇の危険性、通信のブロック、並行処理のおける競合状態、安全なシャットダウン）
// を解決するためには、このような低レイヤーからの設計（アーキテクチャ）が必要不可欠である。
//
// 状態のライフサイクル（所有権・Lifetime）、安全な並行処理機構（Send/Sync, Arc/Mutex, mpsc）、
// 型の力を使った状態遷移（Enum, Dropトレイト）など、
// 「コンパイラと会話しながら、極めて堅牢なサーバーを作り上げる」という体験そのものが、
// Rustを学ぶ最大の理由であり、ただAIに生成させるだけでは得られない「エンジニアリングの真髄」である。

