// 状態共有並行性（Shared-State Concurrency）
    // メッセージ受け渡し（チャンネル）とは別のアプローチ
    // Go言語のスローガン：「メモリを共有することでやり取りするな」
    // なぜ？：チャンネルは単一の所有権（ムーブされる）だが、メモリ共有は「複数の所有権」に似ており、管理の複雑度が増すため。

// Mutex（mutual exclusion = 相互排他）
    // どんな時も「1つのスレッドにしか」データへのアクセスを許可しない仕組み
    // 現実世界で例えるなら「マイクが1つしかないパネルディスカッション」
        // 1. 発言する前にマイク（ロック）を要求する
        // 2. 発言が終わったら次の人にマイク（ロック）を渡す（アンロック）
        // マイクを渡し忘れると、パネルディスカッションが進行不能になる

// Mutexの2つの絶対ルール
    // 1. データを使用する前にロックの獲得を試みなければならない
    // 2. データの使用が終わったら、他のスレッドのためにアンロックしなければならない
// 💡 Rustでは、型システムとスマートポインタの仕組み（DerefとDrop）のおかげで、このロック/アンロック管理を自動で行える

use std::sync::{Arc, Mutex};

fn mutex_single() {
    // MutexのAPIを理解するため、まずはシングルスレッドの文脈で使ってみる
    let m = Mutex::new(5);

    {
        // lockメソッドでロックを獲得する
            // 現在のスレッドをブロックし、順番が来るまで待機する
            // 他のスレッドがロックを持ったままパニックした場合、失敗を通知するので unwrap で処理する
        let mut num = m.lock().unwrap();

        // 内部のデータに変更を加える（Derefトレイトの力）
            // lock()は LockResult に包まれた MutexGuard というスマートポインタを返す
            // MutexGuard は Deref トレイトを実装しているため、自動で参照外し（*num）して内部データにアクセスできる
        *num = 6;

    } // 💡 アンロックの自動化（Dropトレイトの力）
      // MutexGuard は Drop トレイトを実装している！
      // スコープを抜ける（内部スコープの終わり）と自動的にドロップされ、ロックが解除される。
      // これにより「アンロックし忘れ」という致命的なヒューマンエラーが最初から防がれている！

    println!("m = {:?}", m);
}


// 複数スレッド間でMutex<T>を共有する
use std::thread;
fn mutex_multi() {
    // 💡 【Arc<T>による複数所有権とスレッドセーフ】
    // 複数のスレッドに「counter」の所有権を持たせる（複数所有権）必要があるためスマートポインタで包む。
    // ※ 第15章の Rc<T> は参照カウントを非アトミックに操作するため、スレッドセーフではなくコンパイルエラーになる。
    // その代わり、並行性向けに「アトミック（不可分）な参照カウント」を行う Arc<T> (Atomic Reference Counted) を使う。
    let counter = Arc::new(Mutex::new(0));
    let mut handles = vec![];

    // 10個のスレッドを立ち上げて、それぞれがカウンターに1を足そうとする
    // 💡 `for _ in` の `_`（アンダースコア）について：
    // ループの回数（10回）だけ回したいが、現在のインデックス値（0, 1, 2...）自体は
    // ブロック内で使わない場合、変数名の代わりに `_` を使うことで「この値は使いません」とコンパイラに明示できる。
    for _ in 0..10 {
        // 各スレッドにムーブする前に、Arcの参照カウントを増やす（クローンする）
        // これにより、複数スレッドで安全に Mutex への参照を共有できる
        let counter = Arc::clone(&counter);
        
        let handle = thread::spawn(move || {
            // ロックを獲得してデータを更新
            let mut num = counter.lock().unwrap();
            *num += 1;
        });
        handles.push(handle);
    }

    // 全てのスレッドの完了を待機する
    for handle in handles {
        handle.join().unwrap();
    }

    // Arcの参照を通じてロックを獲得し、最終結果を出力する
    println!("結果: {}", *counter.lock().unwrap());
}

fn main() {
    mutex_single();
    mutex_multi();
}

