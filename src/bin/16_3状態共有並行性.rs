// 💡 まとめと発展：第15章（スマートポインタ）との類似性と、並行処理のリスク
// =========================================================================

// 1. 【RefCell<T>/Rc<T> と Mutex<T>/Arc<T> の類似性】
//    - Arc<T> 自体は不変（immutable）な参照を複数スレッドに配るだけ。
//    - しかし、Arc の中身である Mutex<T> は、不変参照を通して「内部の値への可変参照」を獲得できる。
//    - これは第15章で学んだ「内部可変性（Interior Mutability）」そのもの。
//    - つまり、Mutex<T> はスレッドセーフ版の RefCell<T> のような役割を果たしている！
//
//      [シングルスレッド向け]
//      Rc<T>      : 複数の所有者を持てる
//      RefCell<T> : 内部可変性を提供する
//
//      [マルチスレッド向け]
//      Arc<T>     : 複数の所有者を持てる（スレッドセーフ）
//      Mutex<T>   : 内部可変性を提供する（スレッドセーフ）

// 2. 【コンパイラが防げないロジックエラー：デッドロック】
//    - Rustの強力な所有権システムをもってしても、並行処理の「ロジックエラー」からは完全には守ってくれない。
//    - 第15章では、Rc<T> と RefCell<T> の組み合わせが「循環参照（メモリリーク）」を引き起こすリスクを持っていた。
//    - 全く同様に、Mutex<T> と Arc<T> の組み合わせは「デッドロック（Deadlock）」を引き起こすリスクを伴う。
//
//    ※ デッドロックの例：
//       スレッドAが リソース1 のロックを獲得し、次に リソース2 のロックを待つ。
//       同時に、スレッドBが リソース2 のロックを獲得し、次に リソース1 のロックを待つ。
//       結果、お互いがお互いを永遠に待ち続け、プログラムが完全に停止してしまう（パニックすら起きない！）。
//
//    💡 並行処理を扱う際は、常にこの「デッドロックのリスク」が伴うことを認識し、設計段階で緩和策を考慮する必要がある。
